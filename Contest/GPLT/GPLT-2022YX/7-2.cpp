#include<bits/stdc++.h>
using namespace std;
using i32 = long long;

int main() {
    i32 a, b;
    cin >> a >> b;
    printf("%lld+%lld=%lld\n", a,b,a+b);
    printf("%lld-%lld=%lld\n", a,b,a-b);
    printf("%lld*%lld=%lld\n", a,b,a*b);
    printf("%lld/%lld=%lld\n", a,b,a/b);
    printf("%lld%%%lld=%lld\n", a,b,a%b);
    return 0;
}
